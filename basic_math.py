def main():
    print("5-3={}".format(5 - 3))
    print("2+10={}".format(2 + 10))
    print("8*5={}".format(8 * 5))
    print("4/3={}".format(4 / 3))
    print("1/6={}".format(1 / 6))
    print("2+5*7={}".format(2 + 5 * 7))
    print("2^3={}".format(2 ^ 3))  # bitwise operators
    print("2**4={}".format(2**4))
    print("16**5={}".format(16**5))


if __name__ == "__main__":
    main()
